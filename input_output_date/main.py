# выводим одно слово
print('Hello')
#выводим несколько слов
print('Hello world')
#выводим набор символов
print('dfghjncdej')
#выводим целое число
print(1000)
#выводим число с плавающей точкой
print(30.2)
#выводим истину
print(True)
#выводим ложь
print(False)
#выводим none
print(None)
#строка целиком
print('Привет, меня зовут Кеша, мне 2 года ')
#вывод двух переменных
name = 'Кеша'
age = 2
print(name, age)
#вывод через слэш "/" sep=
print(name, age, sep='/')
#перенос строки end
print(name)
print(age)
print('-------->')
print(name, end=';')
print(age, end=';')
print('end', end=';')
